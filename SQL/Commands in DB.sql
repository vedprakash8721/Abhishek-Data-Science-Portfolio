-- there are 5 types of sql commands :
# 1: DDL : data definition language: ( create , alter, rename, truncate, and drop )
# 2: DML : data manipulation language: ( insert , update and delete )
# 3:  DQL : data query language: ( select )
# 4: DCL : data control language: ( grant and revoke , used to grant the users )
#5: TCL : transaction control language: ( start, commit, rollback )
